% 求解下列非线性方程组在原点附近的根
% 9 * x^2 + 36 * y^2 + 4 * z^2 = 36
% x^2 - 2 * y^2 - 20 * z = 0
% 16 * x - x^3  - 2 * y^2 - 16 * z^2 = 0

fun = @(x) [9 * x(1)^2 + 36 * x(2)^2 + 4 * x(3)^2 - 36;
            x(1)^2 - 2 * x(2)^2 - 20 * x(3);
            16 * x(1) - x(1)^3 - 2 * x(2)^2 - 16 * x(3)^2];

% 定义初始点
x0 = [0; 0; 0];

% 使用 fsolve 函数求解非线性方程组
x = fsolve(fun, x0);

% 显示结果
disp(x);



